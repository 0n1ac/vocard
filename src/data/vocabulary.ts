// Helper function to get all words from all sets
export const getAllWords = (): Word[] => {
    return vocabularySets.flatMap((set) => set.words);
};

// Helper function to get a set by ID
export const getVocabularySet = (id: string): VocabularySet | undefined => {
    return vocabularySets.find((set) => set.id === id);
};

// Helper function to get a word by ID
export const getWord = (id: string): Word | undefined => {
    return getAllWords().find((word) => word.id === id);
};
